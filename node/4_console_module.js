// o modulo console possibilita interagir com a linha de comando e funciona da
// mesma forma que o console do browser

console.log(); // imprime string no console

// é possível também passar formatos e variaveis como em um printf
console.log('My %s is %d years old', 'dog', 5); // My dog is 5 years old
/**
 * %s -> string
 * $d -> number
 * %i -> inteiro
 * %o -> variável ou objeto
 */

console.clear(); // limpa o console

